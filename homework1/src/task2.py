print("Data Types in Python")
#Int
a = 10
print(f"a = {int(a)}")
print(type(a))

#Float
b = 1.234
print(f"b = {float(b)}")
print(type(b))

#String
c = "string"
print(f"c = {str(c)}")
print(type(c))

#Bool
d = True
print(f"d = {bool(d)}")
print(type(d))

print("a + b float")
print(a + b)
print("a + b int")
print(int(a + b))